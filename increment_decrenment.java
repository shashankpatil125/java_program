public class increment_decrenment {
    public static void main(String[] args) {
        int a=5;
        int b=5;
        int c=5;
        int d=5;

        //post increnment
        System.out.println(a);
        a++;
        System.out.println(a);
        //pri increnment
        System.out.println(b);
        ++b;
        System.out.println(b);
        //post decrenment
        System.out.println(c);
        c--;
        System.out.println(c);
        //pri decrenment
        System.out.println(d);
        --d;
        System.out.println(d);

    }
}
